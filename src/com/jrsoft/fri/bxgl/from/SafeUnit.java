package com.jrsoft.fri.bxgl.from;

public class SafeUnit {
	private String name;					//保险单位名称
	private int num;						//共投保电梯数
	private int claimNum;			//受理理赔次数
	private  String claimRate;			//理赔率
	
	
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public int getNum() {
		return num;
	}
	public void setNum(int num) {
		this.num = num;
	}
	public int getClaimNum() {
		return claimNum;
	}
	public void setClaimNum(int claimNum) {
		this.claimNum = claimNum;
	}
	public String getClaimRate() {
		return claimRate;
	}
	public void setClaimRate(String claimRate) {
		this.claimRate = claimRate;
	}
	
	
	

}
